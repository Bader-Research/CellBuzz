typedef struct _gzip_header_extra_t {
	unsigned int transparent;
	unsigned int extra_block_info_present;
	unsigned int num_cell_gzip_block;
	unsigned int a_cell_gzip_block_size[65535];
} gzip_header_extra_t;

                        /* gzip header functions */

